CREATE TABLE pokemon(
    id INT PRIMARY KEY,
    name VARCHAR(100),
    type1 VARCHAR(100),
    type2 VARCHAR(100),
    total VARCHAR(100),
    hp VARCHAR(100),
    attack VARCHAR(100),
    defense VARCHAR(100),
    specialAttack VARCHAR(100),
    specialDefense VARCHAR(100),
    speed VARCHAR(100),
    generation VARCHAR(100),
    legendary VARCHAR(100)
)